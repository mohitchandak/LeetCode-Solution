class Solution
{
    public:
    Node* reverseBetween(Node* head, int m, int n)
    {
        Node*temp1= head,*temp2=head;
      vector<int>v;
      while(temp1){
          v.push_back(temp1->data);
          temp1=temp1->next;
      }
      int i=0;
     reverse(v.begin()+m-1,v.begin()+n);  
     while(temp2){
         temp2->data=v[i];
         i++;
         temp2=temp2->next;
      }
     return head;
    }
};

